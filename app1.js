function applyTheme(theme) {
  document.documentElement.setAttribute('data-color-scheme', theme);
  const themeIcon = elements.themeToggle.querySelector('.theme-icon');
  themeIcon.textContent = theme === 'dark' ? '☀️' : '🌙';
  localStorage.setItem('jee-tracker-theme', theme);
}
function toggleTheme() {
  appState.currentTheme = appState.currentTheme === 'light' ? 'dark' : 'light';
  applyTheme(appState.currentTheme);
}
function setupEventListeners() {
  elements.themeToggle.addEventListener('click', toggleTheme);
  elements.subjectButtons.forEach(button => {
    button.addEventListener('click', (e) => {
      const subject = e.currentTarget.dataset.subject;
      selectSubject(subject);
    });
  });
  elements.classButton.addEventListener('click', (e) => {
    e.stopPropagation(); toggleDropdown('class');
  });
  elements.unitButton.addEventListener('click', (e) => {
    e.stopPropagation(); toggleDropdown('unit');
  });
  elements.notStartedFilter.addEventListener('click', () => {
    appState.showNotStarted = !appState.showNotStarted;
    elements.notStartedFilter.classList.toggle('active', appState.showNotStarted);
    filterChapters(); renderChapters();
  });
  elements.weakChapterFilter.addEventListener('click', () => {
    appState.showWeakChapters = !appState.showWeakChapters;
    elements.weakChapterFilter.classList.toggle('active', appState.showWeakChapters);
    filterChapters(); renderChapters();
  });
  elements.sortButton.addEventListener('click', () => {
    appState.sortAscending = !appState.sortAscending;
    const sortText = elements.sortButton.querySelector('.sort-text');
    sortText.textContent = appState.sortAscending ? 'A-Z' : 'Z-A';
    filterChapters(); renderChapters();
  });
  document.addEventListener('click', () => { closeAllDropdowns(); });
  setupClassCheckboxListeners();
}
function setupClassCheckboxListeners() {
  const classCheckboxes = elements.classDropdown.querySelectorAll('input[type="checkbox"]');
  classCheckboxes.forEach(checkbox => {
    checkbox.addEventListener('change', (e) => {
      const value = e.target.value;
      if (e.target.checked) {
        if (!appState.selectedClasses.includes(value)) appState.selectedClasses.push(value);
      } else {
        appState.selectedClasses = appState.selectedClasses.filter(c => c !== value);
      }
      updateClassButtonText(); updateUnitsDropdown(); filterChapters(); renderChapters();
    });
  });
}
function selectSubject(subject) {
  appState.currentSubject = subject;
  elements.subjectButtons.forEach(btn => {
    btn.classList.remove('active');
    if (btn.dataset.subject === subject) btn.classList.add('active');
  });
  appState.selectedClasses = [];
  appState.selectedUnits = [];
  appState.showNotStarted = false;
  appState.showWeakChapters = false;
  updateClassButtonText(); updateUnitsDropdown();
  elements.notStartedFilter.classList.remove('active');
  elements.weakChapterFilter.classList.remove('active');
  // Update main header
  elements.mainSubjectTitle.textContent = subject + " PYQs";
  elements.mainSubjectSubtitle.textContent = subjectConfig[subject].subtitle;
  elements.mainSubjectIcon.textContent = subjectConfig[subject].icon;
  elements.mainSubjectIcon.style.color = subject === 'Physics' ? '#ff9800' : subject === 'Chemistry' ? '#43a047' : '#1976d2';
  // Update TOP header - both title and subtitle separately
  document.getElementById('topMainTitle').textContent = subject + " PYQs";
  document.getElementById('topSubtitle').textContent = "Chapter-wise collection of " + subject + " PYQs";
  filterChapters(); renderChapters();
}


function toggleDropdown(type) {
  const isClass = type === 'class';
  const button = isClass ? elements.classButton : elements.unitButton;
  const dropdown = isClass ? elements.classDropdown : elements.unitDropdown;
  const otherDropdown = isClass ? elements.unitDropdown : elements.classDropdown;
  const otherButton = isClass ? elements.unitButton : elements.classButton;
  otherDropdown.classList.remove('show');
  otherButton.classList.remove('active');
  const isOpen = dropdown.classList.contains('show');
  dropdown.classList.toggle('show', !isOpen);
  button.classList.toggle('active', !isOpen);
}
function closeAllDropdowns() {
  elements.classDropdown.classList.remove('show');
  elements.unitDropdown.classList.remove('show');
  elements.classButton.classList.remove('active');
  elements.unitButton.classList.remove('active');
}
function updateClassButtonText() {
  const selectedText = appState.selectedClasses.length === 0 ? 'All Classes' :
    appState.selectedClasses.length === 1 ? appState.selectedClasses[0] :
      `${appState.selectedClasses.length} selected`;
  elements.classButton.querySelector('.selected-text').textContent = selectedText;
}
function updateUnitsDropdown() {
  const subjectChapters = appState.allChapters.filter(ch => ch.subject === appState.currentSubject);
  const classFilteredChapters = appState.selectedClasses.length > 0 ?
    subjectChapters.filter(ch => appState.selectedClasses.includes(ch.class)) : subjectChapters;
  const units = [...new Set(classFilteredChapters.map(ch => ch.unit))].sort();
  elements.unitDropdown.innerHTML = '';
  units.forEach(unit => {
    const optionDiv = document.createElement('div');
    optionDiv.className = 'multi-select-option';
    const checkbox = document.createElement('input');
    checkbox.type = 'checkbox';
    checkbox.id = `unit-${unit.replace(/\s+/g, '-')}`;
    checkbox.value = unit;
    checkbox.checked = appState.selectedUnits.includes(unit);
    const label = document.createElement('label');
    label.htmlFor = checkbox.id;
    label.textContent = unit;
    optionDiv.appendChild(checkbox);
    optionDiv.appendChild(label);
    elements.unitDropdown.appendChild(optionDiv);
    checkbox.addEventListener('change', (e) => {
      const value = e.target.value;
      if (e.target.checked) {
        if (!appState.selectedUnits.includes(value)) appState.selectedUnits.push(value);
      } else {
        appState.selectedUnits = appState.selectedUnits.filter(u => u !== value);
      }
      updateUnitButtonText(); filterChapters(); renderChapters();
    });
  });
  updateUnitButtonText();
}
function updateUnitButtonText() {
  const selectedText = appState.selectedUnits.length === 0 ? 'All Units' :
    appState.selectedUnits.length === 1 ? appState.selectedUnits[0] :
      `${appState.selectedUnits.length} selected`;
  elements.unitButton.querySelector('.selected-text').textContent = selectedText;
}
function filterChapters() {
  let filtered = appState.allChapters.filter(ch => ch.subject === appState.currentSubject);
  if (appState.selectedClasses.length > 0)
    filtered = filtered.filter(ch => appState.selectedClasses.includes(ch.class));
  if (appState.selectedUnits.length > 0)
    filtered = filtered.filter(ch => appState.selectedUnits.includes(ch.unit));
  if (appState.showNotStarted)
    filtered = filtered.filter(ch => ch.status === 'Not Started');
  if (appState.showWeakChapters)
    filtered = filtered.filter(ch => ch.isWeakChapter);
  filtered.sort((a, b) => {
    const comparison = a.chapter.localeCompare(b.chapter);
    return appState.sortAscending ? comparison : -comparison;
  });
  appState.filteredChapters = filtered;
  elements.chapterCountText.textContent = `Showing all chapters (${filtered.length})`;
}
function renderChapters() {
  elements.chapterList.innerHTML = '';
  appState.filteredChapters.forEach(chapter => {
    elements.chapterList.appendChild(createChapterElement(chapter));
  });
}
function createChapterElement(chapter) {
  const totalQuestions = Object.values(chapter.yearWiseQuestionCount).reduce((sum, count) => sum + count, 0);
  const trend2024 = chapter.yearWiseQuestionCount['2024'] || 0;
  const trend2025 = chapter.yearWiseQuestionCount['2025'] || 0;
  const trendDirection = trend2025 > trend2024 ? 'up' : trend2025 < trend2024 ? 'down' : 'neutral';
  const trendIcon = trendDirection === 'up' ? '↑' : trendDirection === 'down' ? '↓' : '';
  const div = document.createElement('div');
  div.className = 'chapter-item';
  div.innerHTML = `
    <div class="chapter-header">
      <div class="chapter-info">
        <h3 class="chapter-name">${chapter.chapter}</h3>
        <div class="chapter-meta">
          <span class="meta-tag">${chapter.class}</span>
          <span class="meta-tag">${chapter.unit}</span>
        </div>
      </div>
      <div class="chapter-indicators">
        <span class="status-badge status--${chapter.status.toLowerCase().replace(' ', '-')}">${chapter.status}</span>
        ${chapter.isWeakChapter ? '<span class="weak-indicator">&#9888;&#65039; Weak</span>' : ''}
      </div>
    </div>
    <div class="chapter-stats">
      <span class="trend-indicator trend-${trendDirection}">
        2025: ${trend2025}Qs ${trendIcon} | 2024: ${trend2024}Qs
      </span>
      <span class="question-count">
        ${chapter.questionSolved}/${totalQuestions} Qs
      </span>
    </div>
  `;
  return div;
}

